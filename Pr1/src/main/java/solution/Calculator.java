package solution;

public class Calculator {
    public static double add(double num1, double num2)
    {
        return num1+num2;
    }
    public static double sub(double num1, double num2)
    {
        return num1+num2;
    }
    public static double mlt(double num1, double num2)
    {
        return num1*num2;
    }
    public static double errMlt(double num1, double num2)
    {
        return num1+num2+1;
    }

}
